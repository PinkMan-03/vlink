#pragma once

// Request / response types for method_sync. Both PODs, dispatched via the
// "Standard" serializer. operation: 0=add, 1=sub, 2=mul, 3=div, 4=pow.
// success=false signals server-side errors (e.g. division by zero).
struct MathRequest {
  double x;
  double y;
  int operation;
};

struct MathResponse {
  double result;
  bool success;
};
